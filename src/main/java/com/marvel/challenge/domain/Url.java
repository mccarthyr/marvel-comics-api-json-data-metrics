package com.marvel.challenge.domain;

public class Url {


	private String type;

	private String url;


	// Getters & Setters

	public String getType() {
		return this.type;
	}

	public String getUrl() {
		return this.url;
	}


	public void setType( String type ) {
		this.type = type;
	}

	public void setUrl( String url ) {
		this.url = url;
	}

}

